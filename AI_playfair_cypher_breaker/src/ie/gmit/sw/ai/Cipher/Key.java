package ie.gmit.sw.ai.Cipher;

/***
 * Abstract Class representing a Key.
 * 
 * @author RnDMizeD
 *
 */
public abstract class Key {

	/***
	 * Method to retrieve the key.
	 * 
	 * @return this key.
	 */
	public abstract String getKey();
	
	/***
	 * Method to set the key to a given key.
	 * 
	 * @param key This is the key to be set.
	 */
	public abstract void setKey(String key);
	
	
}
